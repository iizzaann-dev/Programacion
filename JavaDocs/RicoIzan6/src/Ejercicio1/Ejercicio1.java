package Ejercicio1;

public class Ejercicio1 {

	public static void main(String[] args) {
		
		int contador = 1;						//Declaramos la variable
		
		System.out.println(contador);
		while(contador <100) {					//Realizamos la condición
			contador++;
			System.out.println(contador);
		}
		
		

	}

}
