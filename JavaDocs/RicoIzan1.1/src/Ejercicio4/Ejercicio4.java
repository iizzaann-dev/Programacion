package Ejercicio4;

public class Ejercicio4 {

	public static void main(String[] args) {
		
		//A continuación declaramos las variables que se solicitan
		byte variable1;
		short variable2;
		int variable3;
		char variable4;
		double variable5;
		float variable6;
		String variable7;
		
		//A continuación se imprime en pantalla los ejemplos de las variables solicitadas
		variable1 = 126;
		System.out.println("Un ejemplo de una variable de tipo byte es: " + variable1);
		
		variable2 = 30000;
		System.out.println("Un ejemplo de una variable de tipo short es: " + variable2);
		
		variable3 = 33;
		System.out.println("Un ejemplo de una variable de tipo entero es: " + variable3);
		
		variable4 = 'z';
		System.out.println("Un ejemplo de una variable de tipo carácter es: " + variable4);
		
		variable5 = 18.5;
		System.out.println("Un ejemplo de una variable de tipo  double es: " + variable5);
		
		variable6 = 3.14f;
		System.out.println("Un ejemplo de una variable de tipo float es: " + variable6);
		
		variable7 = "¡Hola Mundo!";
		System.out.print("Un ejemplo de una variable de tipo cadena de caracteres es: " + variable7);
		
		
		
	}

}
